def get_juz_page_range(juz_number: int):
    """Return (first_page, last_page) of the juz in the 604-page Madinah Mushaf."""
    n = int(juz_number)
    if n < 1 or n > 30:
        return (0, 0)
    if n == 1:
        return (1, 21)
    if n == 30:
        return (582, 604)
    start = 2 + (n - 1) * 20
    return (start, start + 19)


def get_juz_display_name(juz_number: int) -> str:
    """Human-friendly Arabic juz name, e.g. 'الجزء الثلاثون (عمّ)'."""
    n = int(juz_number)
    if n < 1 or n > 30:
        return ""
    return f"الجزء {AR_JUZ_ORDINALS[n - 1]} ({JUZ_NAMES[n]})"