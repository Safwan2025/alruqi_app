def get_ayah_page(surah_number: int, ayah_number: int) -> int:
    """Exact Madinah Mushaf page for a given (surah, ayah).

    Uses the standard 604-page Madinah Mushaf page-start table
    (`PAGE_STARTS`). Finds the largest page whose start position
    (start_surah, start_ayah) is <= (surah_number, ayah_number).
    """
    if not surah_number or surah_number < 1 or surah_number > 114:
        return 0
    a = max(1, int(ayah_number or 1))
    s = int(surah_number)
    # Binary search across PAGE_STARTS (sorted by surah then ayah)
    lo, hi = 0, len(PAGE_STARTS) - 1
    best = 0
    while lo <= hi:
        mid = (lo + hi) // 2
        ms, ma = PAGE_STARTS[mid]
        if (ms, ma) <= (s, a):
            best = mid
            lo = mid + 1
        else:
            hi = mid - 1
    return best + 1  # pages are 1-indexed


